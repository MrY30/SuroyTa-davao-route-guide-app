class MainBottomSheet extends StatelessWidget {
  final DraggableScrollableController sheetController;
  final int selectedIndex;
  final bool hasSuggestedRoutes;

  final VoidCallback onDrawMapElements;
  final void Function(LatLng coordinates, String name) onLocationFound;
  final Future<void> Function() onShowFloatingCard;

  // These will hold the UI generated by your main.dart methods for now
  // final List<Widget> suggestedRoutesContent;

  const MainBottomSheet({
    super.key,
    required this.sheetController,
    required this.selectedIndex,
    required this.hasSuggestedRoutes,
    required this.onDrawMapElements,
    required this.onLocationFound,
    required this.onShowFloatingCard,
  });

  @override
  Widget build(BuildContext context) {
    return DraggableScrollableSheet(
      controller: sheetController,
      initialChildSize: 0.0,
      minChildSize: 0.0,
      maxChildSize: 0.96,
      snap: true,
      snapSizes: const [0.26],
      builder: (BuildContext context, ScrollController scrollController) {
        return Container(
          decoration: const BoxDecoration(
            color: sheetBackgroundColor, 
            borderRadius: BorderRadius.vertical(top: Radius.circular(20)),
            boxShadow: [BoxShadow(color: Colors.black26, blurRadius: 10)],
          ),
          child: ListView(
            controller: scrollController,
            padding: const EdgeInsets.all(16.0),
            children: [
              // 1. The Drag Handle
              Center(
                child: Container(
                  width: 40,
                  height: 5,
                  margin: const EdgeInsets.only(bottom: 20),
                  decoration: BoxDecoration(
                    color: Colors.black26,
                    borderRadius: BorderRadius.circular(10),
                  ),
                ),
              ),

              // 2. The Content Switcher
              if (selectedIndex == 0) ExploreSheetContent(onDrawMapElements: onDrawMapElements),
              if (selectedIndex == 1 && hasSuggestedRoutes) SuggestedRoutesContent(sheetController: sheetController, onDrawMapElements: onDrawMapElements, onShowFloatingCard: onShowFloatingCard),
              if (selectedIndex == 2) SearchSheetContent(onLocationFound: onLocationFound),
            ],
          ),
        );
      },
    );
  }
}

