class SearchSheetContent extends ConsumerStatefulWidget {
  // The remote control! It passes the coordinates and name back to main.dart
  final void Function(LatLng coordinates, String name) onLocationFound;

  const SearchSheetContent({
    super.key,
    required this.onLocationFound,
  });

  @override
  ConsumerState<SearchSheetContent> createState() => _SearchSheetContentState();
}

class _SearchSheetContentState extends ConsumerState<SearchSheetContent> {
  final GeocodingService _geocodingService = GeocodingService();
  final HiveService _hiveService = HiveService();

  // We moved the History logic here because only the Search tab uses it!
  Future<void> _saveToHistory(String name, LatLng coords) async {
    final history = _hiveService.getHistory();
    history.sort((a, b) => a.id.compareTo(b.id));

    if (history.length >= 5) {
      await _hiveService.deleteLocation(history.first.id);
    }

    final newHistoryItem = FavoriteLocation(
      id: DateTime.now().millisecondsSinceEpoch.toString(),
      name: name,
      latitude: coords.latitude,
      longitude: coords.longitude,
      iconCodePoint: Icons.restore.codePoint,
      isFavorite: false,
    );

    await _hiveService.saveLocation(newHistoryItem);
    setState(() {}); // Refresh the history list on screen
  }

  @override
  Widget build(BuildContext context) {
    // Fetch live data from the local database
    final historyList = _hiveService.getHistory();
    historyList.sort((a, b) => b.id.compareTo(a.id));
    final favoritesList = _hiveService.getFavorites();

    // Wrap the original list of widgets in a Column
    return Column(
      crossAxisAlignment: CrossAxisAlignment.stretch,
      mainAxisSize: MainAxisSize.min,
      children: [
        Text(
          'Asa Ta?',
          style: TextStyle(
            fontSize: 32,
            fontWeight: FontWeight.w900,
            color: primaryColor,
            fontFamily: GoogleFonts.paytoneOne().fontFamily,
          ),
        ),
        const SizedBox(height: 10),
        SizedBox(
          height: 40,
          child: AutoSizeText(
            'Search a location by typing its name or pasting its coordinates from external maps using the Search Bar.',
            style: const TextStyle(color: fontColor, fontSize: 16, height: 1.2),
            maxLines: 2,
            minFontSize: 10,
            overflow: TextOverflow.ellipsis,
          ),
        ),
        const SizedBox(height: 20),

        // 1. THE DUAL-INPUT SEARCH BAR
        Container(
          margin: const EdgeInsets.symmetric(horizontal: 5),
          child: TextField(
            decoration: InputDecoration(
              hintText: 'Search here...',
              prefixIcon: const Icon(Icons.search),
              filled: true,
              fillColor: fontColor,
              border: OutlineInputBorder(
                borderRadius: BorderRadius.circular(15),
                borderSide: BorderSide.none,
              ),
            ),
            onSubmitted: (value) async {
              GeocodeResult? geocodeResult = await _geocodingService.searchLocation(value);

              if (geocodeResult != null) {
                // Tell main.dart to move the map!
                widget.onLocationFound(geocodeResult.coordinates, geocodeResult.name);
                await _saveToHistory(geocodeResult.name, geocodeResult.coordinates);
              }
            },
          ),
        ),
        const SizedBox(height: 24),

        // 2. FAVORITES SECTION
        const Text('Saved Places', style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: fontColor)),
        const SizedBox(height: 10),
        if (favoritesList.isEmpty)
          const Text('No saved places yet. Long press a search result to save it here!', style: TextStyle(color: Colors.grey, fontStyle: FontStyle.italic))
        else
          SizedBox(
            height: 90,
            child: ListView.builder(
              scrollDirection: Axis.horizontal,
              itemCount: favoritesList.length,
              itemBuilder: (context, index) {
                final fav = favoritesList[index];
                return GestureDetector(
                  onTap: () {
                    // Tell main.dart to move the map!
                    widget.onLocationFound(LatLng(fav.latitude, fav.longitude), fav.name);
                  },
                  child: Container(
                    width: 100,
                    margin: const EdgeInsets.only(right: 12),
                    decoration: BoxDecoration(color: cardColor, borderRadius: BorderRadius.circular(15)),
                    child: Column(
                      mainAxisAlignment: MainAxisAlignment.center,
                      children: [
                        Icon(IconData(fav.iconCodePoint ?? 0, fontFamily: 'MaterialIcons'), color: primaryColor, size: 20),
                        const SizedBox(height: 8),
                        Text(fav.name, style: const TextStyle(color: fontColor, fontSize: 14, fontWeight: FontWeight.bold), textAlign: TextAlign.center, maxLines: 1, overflow: TextOverflow.ellipsis),
                      ],
                    ),
                  ),
                );
              },
            ),
          ),
        const SizedBox(height: 24),

        // 3. RECENT SEARCHES SECTION
        const Text('Recent Searches', style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: fontColor)),
        const SizedBox(height: 10),
        if (historyList.isEmpty)
          const Text('Your recent searches will appear here.', style: TextStyle(color: Colors.grey, fontStyle: FontStyle.italic))
        else
          ...historyList.map((historyItem) {
            return ListTile(
              contentPadding: EdgeInsets.zero,
              leading: CircleAvatar(
                backgroundColor: cardColor,
                child: Icon(IconData(historyItem.iconCodePoint ?? 0, fontFamily: 'MaterialIcons'), color: unselectedNavColor),
              ),
              title: Text(historyItem.name, style: const TextStyle(color: fontColor, fontWeight: FontWeight.bold)),
              subtitle: Text('${historyItem.latitude.toStringAsFixed(4)}, ${historyItem.longitude.toStringAsFixed(4)}', style: const TextStyle(color: Colors.grey, fontSize: 12)),
              trailing: IconButton(
                icon: const Icon(Icons.close, color: Colors.grey, size: 20),
                onPressed: () async {
                  await _hiveService.deleteLocation(historyItem.id);
                  setState(() {});
                },
              ),
              onTap: () {
                // Tell main.dart to move the map!
                widget.onLocationFound(LatLng(historyItem.latitude, historyItem.longitude), historyItem.name);
              },
            );
          }),
        const SizedBox(height: 80),
      ],
    );
  }
}

class SuggestedRoutesContent extends ConsumerStatefulWidget {
  final DraggableScrollableController sheetController;
  final VoidCallback onDrawMapElements;
  final Future<void> Function() onShowFloatingCard; // The remote control for the top card!

  const SuggestedRoutesContent({
    super.key,
    required this.sheetController,
    required this.onDrawMapElements,
    required this.onShowFloatingCard,
  });

  @override
  ConsumerState<SuggestedRoutesContent> createState() => _SuggestedRoutesContentState();
}

class _SuggestedRoutesContentState extends ConsumerState<SuggestedRoutesContent> {
  // We moved the Network Service here because only this tab uses it!
  final OsrmService _osrmService = OsrmService();

  @override
  Widget build(BuildContext context) {
    // 1. Read the pins from Riverpod
    final mapState = ref.watch(mapStateProvider);
    final startPin = mapState.startPin;
    final destinationPin = mapState.destinationPin;

    // 2. Read the routes from Riverpod
    final routeState = ref.watch(routeStateProvider);
    final allRoutes = routeState.allRoutes;
    final suggestedRoutes = routeState.suggestedRoutes;

    // 3. Sort the routes safely
    final List<RouteResult> sortedRoutes = List.from(suggestedRoutes);
    sortedRoutes.sort((a, b) {
      int fareComparison = a.estimatedFare.compareTo(b.estimatedFare);
      if (fareComparison != 0) return fareComparison;
      return a.totalEstimatedWalk.compareTo(b.totalEstimatedWalk);
    });

    // 4. Build the UI
    return Column(
      crossAxisAlignment: CrossAxisAlignment.stretch,
      children: [
        Text(
          'Suggested Routes',
          style: TextStyle(
            fontSize: 32,
            fontWeight: FontWeight.bold,
            color: primaryColor,
            fontFamily: GoogleFonts.paytoneOne().fontFamily,
          ),
        ),
        const SizedBox(height: 10),
        const Text(
          'Pick the jeep you wish to ride and it will give you the walking distance, and estimated fare.',
          style: TextStyle(color: fontColor),
        ),
        ...sortedRoutes.asMap().entries.map((entry) {
          final int index = entry.key;
          final RouteResult result = entry.value;
          final bool isBestRoute = index == 0;

          return Card(
            margin: const EdgeInsets.symmetric(vertical: 15.0),
            color: btnColor,
            elevation: 10,
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(20)),
            child: InkWell(
              borderRadius: BorderRadius.circular(20),
              onTap: () async {
                // 1. Tell main.dart to handle the sliding animation logic
                await widget.onShowFloatingCard();

                // 2. Update Riverpod and draw the map
                ref.read(routeStateProvider.notifier).setSelectedRoute(result);
                widget.onDrawMapElements();

                // 3. Slide the bottom sheet out of the way
                if (widget.sheetController.isAttached) {
                  widget.sheetController.animateTo(
                    0.26,
                    duration: const Duration(milliseconds: 300),
                    curve: Curves.easeInOut,
                  );
                }

                // 4. Fetch the walking paths from OSRM
                if (result.actualStartWalk == null && startPin != null && destinationPin != null) {
                  setState(() { result.isFetchingActualRoute = true; });

                  final routeData = allRoutes.firstWhere((r) => r.name == result.routeName);
                  final boardPoint = routeData.polylineData!.points[result.boardIndex];
                  final alightPoint = routeData.polylineData!.points[result.alightIndex];

                  final startLeg = await _osrmService.fetchWalkingRoute(startPin, boardPoint);
                  final endLeg = await _osrmService.fetchWalkingRoute(alightPoint, destinationPin);

                  if (startLeg != null && endLeg != null) {
                    setState(() {
                      result.actualWalkPathStart = startLeg['path'];
                      result.actualWalkPathEnd = endLeg['path'];
                      result.actualStartWalk = startLeg['distance'];
                      result.actualEndWalk = endLeg['distance'];
                    });
                    widget.onDrawMapElements();
                  }

                  setState(() { result.isFetchingActualRoute = false; });
                }
              },
              child: Container(
                height: 60,
                padding: const EdgeInsets.symmetric(horizontal: 16),
                child: Row(
                  children: [
                    isBestRoute
                        ? const Icon(Icons.star_outline, size: 35, color: fontColor)
                        : Image.asset('assets/images/jeep_logo.png', width: 35, height: 35),
                    const SizedBox(width: 16),
                    Expanded(
                      child: Column(
                        mainAxisAlignment: MainAxisAlignment.center,
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Flexible(
                            child: FittedBox(
                              fit: BoxFit.scaleDown,
                              alignment: Alignment.centerLeft,
                              child: Text(
                                result.routeName,
                                style: TextStyle(
                                  color: fontColor,
                                  fontSize: 25,
                                  fontFamily: GoogleFonts.paytoneOne().fontFamily,
                                  height: 1.0,
                                  shadows: [Shadow(offset: const Offset(1, 4), blurRadius: 7, color: Colors.black.withOpacity(0.5))],
                                ),
                              ),
                            ),
                          ),
                          if (isBestRoute)
                            const Padding(
                              padding: EdgeInsets.only(top: 2.0),
                              child: Text('RECOMMENDED', style: TextStyle(color: primaryColor, fontSize: 10, fontWeight: FontWeight.bold, height: 1.0)),
                            ),
                        ],
                      ),
                    ),
                    const SizedBox(width: 16),
                    result.isFetchingActualRoute
                        ? const SizedBox(width: 16, height: 16, child: CircularProgressIndicator(strokeWidth: 2))
                        : const Icon(Icons.arrow_forward_ios, size: 16, color: fontColor),
                  ],
                ),
              ),
            ),
          );
        }),
        const SizedBox(height: 80),
      ],
    );
  }
}